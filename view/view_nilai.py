from view.input_nilai import *


def cetak_daftar_nilai():
    no = 0
    if len(nim) != 0:
        print("\nDaftar Nilai")
        print("======================================================================================")
        print("| No |     NIM     |    NAMA    |    TUGAS    |    UTS    |    UAS    |    AKHIR     |")
        print("======================================================================================")
        for i in range(len(nim)):
            no = no + 1
            print("|{0:>3d} | {1:12s}| {2:11.11s}|{3:^13d}|{4:^11d}|{5:^11d}|{6:^14.2f}|".format(no, nim[i]['nim'],
                                                                                                 nama[i]['nama'],
                                                                                                 tugas[i]['tugas'],
                                                                                                 uts[i]['uts'],
                                                                                                 uas[i]['uas'],
                                                                                                 nakhir[i]['nakhir']))
        print("======================================================================================")
    else:
        print("\nDaftar Nilai")
        print("======================================================================================")
        print("| No |     NIM     |    NAMA    |    TUGAS    |    UTS    |    UAS    |     AKHIR    |")
        print("======================================================================================")
        print("|                                TIDAK ADA DATA                                      |")
        print("======================================================================================")
